#!/usr/bin/env node
/*
 * scripts/audit-example-picker.js
 *
 * Audit script for Phase 3 (Ask 3) of the vocab flashcard enhancement.
 * Builds a runtime-shaped index mapping vocabId -> [sentences] by scanning
 * every N4/N5 lesson's warmup.items[], reading.passage[], and
 * conversation.lines[] for tagged sentences. Applies the selection
 * heuristic (home-lesson first; priority warmup > reading > conversation;
 * tie-break shortest jp) and writes a per-vocab report to
 * scripts/output/example-picker-audit.txt.
 *
 * Run: node scripts/audit-example-picker.js
 */

'use strict';

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const MANIFEST = path.join(ROOT, 'manifest.json');
const OUT_DIR = path.join(ROOT, 'scripts', 'output');
const OUT_FILE = path.join(OUT_DIR, 'example-picker-audit.txt');

const SECTION_PRIORITY = { warmup: 1, reading: 2, conversation: 3 };

function termId(term) {
  if (typeof term === 'string') return term;
  if (term && typeof term === 'object' && term.id) return term.id;
  return null;
}

function extractSentences(lessonJson) {
  const lessonId = lessonJson.id || '?';
  const out = [];
  for (const section of (lessonJson.sections || [])) {
    const type = section.type;
    if (type === 'warmup') {
      for (const item of (section.items || [])) {
        if (item && item.jp && Array.isArray(item.terms)) {
          out.push({
            jp: item.jp,
            en: item.en || '',
            terms: item.terms,
            lessonId: lessonId,
            sectionType: 'warmup'
          });
        }
      }
    } else if (type === 'reading') {
      const passage = Array.isArray(section.passage) ? section.passage : [];
      for (const item of passage) {
        if (item && item.jp && Array.isArray(item.terms)) {
          out.push({
            jp: item.jp,
            en: item.en || '',
            terms: item.terms,
            lessonId: lessonId,
            sectionType: 'reading'
          });
        }
      }
    } else if (type === 'conversation') {
      for (const line of (section.lines || [])) {
        if (line && line.jp && Array.isArray(line.terms)) {
          out.push({
            jp: line.jp,
            en: line.en || '',
            terms: line.terms,
            lessonId: lessonId,
            sectionType: 'conversation'
          });
        }
      }
    }
  }
  return out;
}

function buildIndex(allSentences) {
  const idx = {};
  for (const sent of allSentences) {
    const seen = new Set();
    for (const term of sent.terms) {
      const id = termId(term);
      if (!id || seen.has(id)) continue;
      seen.add(id);
      if (!idx[id]) idx[id] = [];
      idx[id].push(sent);
    }
  }
  return idx;
}

function pickExample(vocabId, homeLessonId, index) {
  const pool = index[vocabId];
  if (!pool || pool.length === 0) return null;

  const scored = pool.map((s, i) => ({
    sent: s,
    home: homeLessonId && s.lessonId === homeLessonId ? 0 : 1,
    sectionRank: SECTION_PRIORITY[s.sectionType] || 9,
    jpLen: s.jp.length,
    origIdx: i
  }));

  scored.sort((a, b) => {
    if (a.home !== b.home) return a.home - b.home;
    if (a.sectionRank !== b.sectionRank) return a.sectionRank - b.sectionRank;
    if (a.jpLen !== b.jpLen) return a.jpLen - b.jpLen;
    return a.origIdx - b.origIdx;
  });

  return scored[0].sent;
}

function main() {
  const manifest = JSON.parse(fs.readFileSync(MANIFEST, 'utf8'));
  const levels = manifest.levels || ['N5', 'N4'];

  const allSentences = [];
  const glossaries = [];

  for (const lvl of levels) {
    const levelData = manifest.data[lvl] || {};
    const glossaryPath = path.join(ROOT, levelData.glossary);
    const glossary = JSON.parse(fs.readFileSync(glossaryPath, 'utf8'));
    glossaries.push({ level: lvl, entries: glossary.entries });

    for (const lessonEntry of (levelData.lessons || [])) {
      const lessonPath = path.join(ROOT, lessonEntry.file);
      if (!fs.existsSync(lessonPath)) {
        console.warn('missing lesson file:', lessonPath);
        continue;
      }
      try {
        const lessonJson = JSON.parse(fs.readFileSync(lessonPath, 'utf8'));
        allSentences.push(...extractSentences(lessonJson));
      } catch (e) {
        console.warn('failed to parse', lessonPath, e.message);
      }
    }
  }

  const index = buildIndex(allSentences);

  // Build report
  if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });

  const lines = [];
  const summary = {
    total: 0,
    noMatch: 0,
    noEn: 0,
    longJp: 0,
    jpEqualsSurface: 0
  };

  lines.push('# Vocab example-picker audit');
  lines.push('# Generated by scripts/audit-example-picker.js');
  lines.push('# Heuristic: home lesson first; section priority warmup > reading > conversation; tie-break shortest jp.');
  lines.push('');

  const flagged = [];

  for (const g of glossaries) {
    lines.push('## ' + g.level);
    lines.push('');
    for (const entry of g.entries) {
      if (entry.type !== 'vocab') continue;
      summary.total++;

      const homeLesson = entry.lesson_ids || '';
      const picked = pickExample(entry.id, homeLesson, index);
      const altCount = ((index[entry.id] || []).length) - (picked ? 1 : 0);

      let line = entry.id + '  ' + (entry.surface || '') + ' (' + (entry.reading || '') + ') "' + (entry.meaning || '') + '"  [home:' + (homeLesson || '—') + ']';

      if (!picked) {
        summary.noMatch++;
        line += '\n  PICK: (none)';
        flagged.push({ type: 'noMatch', line: line });
      } else {
        line += '\n  PICK: [' + picked.lessonId + ' ' + picked.sectionType + '] ' + picked.jp + (picked.en ? ' — ' + picked.en : ' — (no en)');
        if (altCount > 0) line += '\n  alts: ' + altCount + ' other sentence' + (altCount > 1 ? 's' : '') + ' found';

        const flags = [];
        if (!picked.en) { flags.push('NO_EN'); summary.noEn++; }
        if (picked.jp.length > 40) { flags.push('LONG_JP(' + picked.jp.length + ')'); summary.longJp++; }
        if (entry.surface && picked.jp.trim() === entry.surface.trim()) { flags.push('JP_EQUALS_SURFACE'); summary.jpEqualsSurface++; }
        if (flags.length) {
          line += '\n  FLAGS: ' + flags.join(', ');
          flagged.push({ type: flags.join(','), line: line });
        }
      }
      lines.push(line);
      lines.push('');
    }
  }

  // Flag summary at the top of the file
  const header = [
    '# Summary',
    '# total vocab: ' + summary.total,
    '# no match: ' + summary.noMatch,
    '# picked has no en: ' + summary.noEn,
    '# picked jp > 40 chars: ' + summary.longJp,
    '# picked jp equals vocab surface: ' + summary.jpEqualsSurface,
    '',
    '# Flagged entries (' + flagged.length + '):',
    ''
  ];
  for (const f of flagged) {
    header.push(f.line);
    header.push('');
  }
  header.push('# ---- Full per-vocab report ----');
  header.push('');

  fs.writeFileSync(OUT_FILE, header.concat(lines).join('\n'), 'utf8');

  console.log('Wrote', OUT_FILE);
  console.log('Summary:', summary);
}

main();
